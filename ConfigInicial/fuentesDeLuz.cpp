// Moves/alters the camera positions based on user input
void DoMovement()
{
	// Camera controls
	if (keys[GLFW_KEY_W] || keys[GLFW_KEY_UP])
	{
		camera.ProcessKeyboard(FORWARD, deltaTime);
	}

	if (keys[GLFW_KEY_S] || keys[GLFW_KEY_DOWN])
	{
		camera.ProcessKeyboard(BACKWARD, deltaTime);
	}

	if (keys[GLFW_KEY_A] || keys[GLFW_KEY_LEFT])
	{
		camera.ProcessKeyboard(LEFT, deltaTime);
	}

	if (keys[GLFW_KEY_D] || keys[GLFW_KEY_RIGHT])
	{
		camera.ProcessKeyboard(RIGHT, deltaTime);
	}
}

// Is called whenever a key is pressed/released via GLFW
void KeyCallback(GLFWwindow *window, int key, int scancode, int action, int mode)
{
	if (GLFW_KEY_ESCAPE == key && GLFW_PRESS == action)
	{
		glfwSetWindowShouldClose(window, GL_TRUE);
	}

	if (key >= 0 && key < 1024)
	{
		if (action == GLFW_PRESS)
		{
			keys[key] = true;
		}
		else if (action == GLFW_RELEASE)
		{
			keys[key] = false;
		}
	}

	if (keys[GLFW_KEY_SPACE])
	{
		active = !active;
		if (active)
		{
			Light1 = glm::vec3(1.0f, 1.0f, 0.0f);
		}
		else
		{
			Light1 = glm::vec3(0);//Cuado es solo un valor en los 3 vectores pueden dejar solo una componente
		}
	}

	if (keys[GLFW_KEY_O])
	{
		if (day) {
			angleSun += 2.0f; // Aumenta el ángulo
			if (angleSun > 180.0f) angleSun = 180.0f; // Límite superior
		}
		else {
			angleMoon += 2.0f; // Aumenta el ángulo
			if (angleMoon > 180.0f) angleMoon = 180.0f; // Límite superior
		}

	}

	if (keys[GLFW_KEY_L])
	{
		if (day) {
			angleSun -= 2.0f; // Disminuye el ángulo
			if (angleSun < 0.0f) angleSun = 0.0f; // Límite inferior
		}
		else {
			angleMoon -= 2.0f; // Disminuye el ángulo
			if (angleMoon < 0.0f) angleMoon = 0.0f; // Límite inferior
		}
	}

	if (keys[GLFW_KEY_TAB])
	{
		if (day) {
			Light3 = glm::vec3(1.0f, 1.0f, 1.0f);
			Light2 = glm::vec3(0);
			day = false;
		}
		else {
			Light3 = glm::vec3(0);
			Light2 = glm::vec3(1.0f, 1.0f, 1.0f);
			day = true;
		}
	}

}

void MouseCallback(GLFWwindow *window, double xPos, double yPos)
{
	if (firstMouse)
	{
		lastX = xPos;
		lastY = yPos;
		firstMouse = false;
	}

	GLfloat xOffset = xPos - lastX;
	GLfloat yOffset = lastY - yPos;  // Reversed since y-coordinates go from bottom to left

	lastX = xPos;
	lastY = yPos;

	camera.ProcessMouseMovement(xOffset, yOffset);
}